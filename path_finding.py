def build_well(editor, x, y, z):

    # Foundation and Walls
    for dx in range(-1, 2):
        for dz in range(-1, 2):
            editor.placeBlock((x+dx, y-2, z+dz), Block("cobblestone"))
            editor.placeBlock((x+dx, y-1, z+dz), Block("cobblestone"))
            
            if dx == 0 and dz == 0:
                editor.placeBlock((x, y-1, z), Block("water"))
                editor.placeBlock((x, y, z), Block("air"))
            else:
                editor.placeBlock((x+dx, y, z+dz), Block("cobblestone"))
    
    # Pillars
    editor.placeBlock((x-1, y+1, z-1), Block("cobblestone_wall"))
    editor.placeBlock((x-1, y+1, z+1), Block("cobblestone_wall"))
    editor.placeBlock((x+1, y+1, z-1), Block("cobblestone_wall"))
    editor.placeBlock((x+1, y+1, z+1), Block("cobblestone_wall"))
    
    # Roof
    for dx in range(-1, 2):
        for dz in range(-1, 2):
            editor.placeBlock((x+dx, y+2, z+dz), Block("cobblestone_slab"))

def place_road(editor, heightmap, origin, road_tiles, worldslice):
    for (x, z) in road_tiles:
        y = get_height(heightmap, x, z, origin)
        if y is None: continue

        # Water bridges vs Dirt paths
        if is_water_ws(worldslice, x, z, origin):
            editor.placeBlock((x, y, z), Block("oak_planks"))
            editor.placeBlock((x, y+1, z), Block("air"))
            editor.placeBlock((x, y+2, z), Block("air"))
        else:
            road_block = random.choice(["dirt_path", "dirt_path", "dirt_path", "coarse_dirt"])
            editor.placeBlock((x, y-1, z), Block(road_block))
            editor.placeBlock((x, y, z), Block("air"))
            editor.placeBlock((x, y+1, z), Block("air"))

