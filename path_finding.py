def generate_village(editor, buildArea, heightmap, worldslice, num_houses=10, num_farms=1):
    origin = (buildArea.offset.x, buildArea.offset.z)
    
    # 1. & 2. Spatial Layout utilizing the Jigsaw algorithm
    print("Tracing jigsaw street layouts...")
    houses, farms, road_tiles, center = generate_layout(worldslice, heightmap, origin, num_houses, num_farms)

    # 3. Terrain Modding (Roads and Bridges)
    print("Weaving road network and bridges...")
    place_road(editor, heightmap, origin, road_tiles, worldslice)

    # 4. Central Monument
    print("Building central well...")
    cx, cz = center
    cy = get_height(heightmap, cx, cz, origin)
    if cy is not None: build_well(editor, cx, cy, cz)

    # 5. Build Structures
    palette = get_palette(worldslice, heightmap, origin)
    print("Constructing buildings...")
    
    for hx, hy, hz, depth, facing in houses:
        if random.random() < 0.4:
            build_houses.build_2fhouse(editor, hx, hy, hz, depth, palette, facing)
        else:
            build_houses.build_1fhouse(editor, hx, hy, hz, depth, 4, palette, facing)
            
    for fx, fy, fz in farms:
        build_houses.build_farm(editor, fx, fy, fz, width=9, depth=9, palette=palette)
        
    print(f"Village generation complete. Placed {len(houses)} houses and {len(farms)} farms.")
